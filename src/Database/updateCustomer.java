    public DefaultTableModel updateTable() throws SQLException {
        Query tampil = new Query();
        ResultSet rs = tampil.customerInfo();

        DefaultTableModel dtm = new DefaultTableModel();
        int colum;

        ResultSetMetaData rsmeta = rs.getMetaData();
        colum = rsmeta.getColumnCount() + 1;

        Vector colum_name = new Vector();
        Vector data_rows = new Vector();

        for (int i = 1; i < colum; i++) {
            colum_name.addElement(rsmeta.getColumnName(i));
        }
        dtm.setColumnIdentifiers(colum_name);

        while (rs.next()) {

            data_rows = new Vector();
            for (int j = 1; j < colum; j++) {
                data_rows.addElement(rs.getString(j));
            }
            dtm.addRow(data_rows);
        };
        return dtm;
    }

