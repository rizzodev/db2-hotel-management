    private void okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okActionPerformed

        try {
            updateTable();
            this.key = textPrimary.getText();
            int noFindRow = 0;

            for (int i = 0; i < updateTable().getRowCount(); i++) {
                if (key.equals(updateTable().getValueAt(i, 0))) {
                    noFindRow = i;
                    break;
                } else {
                    noFindRow = -1;
                }
            }

            for (int j = 0; j < updateTable().getColumnCount(); j++) {
                System.out.println("" + updateTable().getValueAt(noFindRow, j));
            }

            SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");

            Date checkInDate;
            Date checkOutDate;

            String d1;
            String d2;

            d1 = (String) updateTable().getValueAt(noFindRow, 5);
            d2 = (String) updateTable().getValueAt(noFindRow, 6);

            checkInDate = format.parse(d1);
            checkOutDate = format.parse(d2);

            tFName.setText((String) updateTable().getValueAt(noFindRow, 1));
            tFGender.setText((String) updateTable().getValueAt(noFindRow, 2));
            tFCountry.setText((String) updateTable().getValueAt(noFindRow, 3));
            tFPhone.setText((String) updateTable().getValueAt(noFindRow, 4));
            txtdateCheckin.setDate(checkInDate);
            txtdateCheckOut.setDate(checkOutDate);
            tFRoomNumber.setText((String) updateTable().getValueAt(noFindRow, 7));

        } catch (Exception e) {
            tFName.setText("");
            tFGender.setText("");
            tFCountry.setText("");
            tFPhone.setText("");
            txtdateCheckin.setDate(null);
            txtdateCheckOut.setDate(null);
            tFRoomNumber.setText("");
            JOptionPane.showMessageDialog(this, "Customer Tidak Ada !");
        }
    }//GEN-LAST:event_okActionPerformed

