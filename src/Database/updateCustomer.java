    public int hitungTotal(String dateStart, String dateStop, int room_number) {
        int lama;
        Date checkIn, checkOut;
        int cost_per_night;
        int total = 0;

        //HH converts hour in 24 hours format (0-23), day calculation
        SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");

        Date d1 = null;
        Date d2 = null;
        try {
            d1 = format.parse(dateStart);
            d2 = format.parse(dateStop);

            //in milliseconds
            long diff = d2.getTime() - d1.getTime();
            long diffDays = diff / (24 * 60 * 60 * 1000);

            // lama = checkOut - checkIn;
            if (room_number == 1) {
                cost_per_night = 100000;
            } else if (room_number == 2) {
                cost_per_night = 200000;
            } else if (room_number == 3) {
                cost_per_night = 200000;
            } else {
                cost_per_night = 300000;
            }

            total = (int) (diffDays * cost_per_night);

            System.out.print(diffDays + " days, ");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Silahkan Masukkan Tanggal Check In/Check Out dengan Benar");
        }

        // total = lama 
        return total;
    }

